/*

    * console.log(2>1);  ==> true
    * console.log(2==1);  ==> false
    * console.log(2<1);  ==> false
    * console.log(2!=1);  ==> true

*/

/*

    ^ console.log("2">1);  ==> true
    ^ console.log("2"<1);  ==> false
    ^ console.log("2"==1);  ==> false
    ^ In This First Type Conversion Is Done Then The Values Are Compared With Each Other.
*/

/*

    & console.log(null>0);  ==> false
    & console.log(null==0);  ==> false
    & console.log(null>=0);  ==> true
    & In This Null Is Treated As 0. 
*/

/*

    ! console.log(undefined>0);  ==> false
    ! console.log(undefined==0);  ==> false
    ! console.log(undefined>=0);  ==> false
    ! In This Undefined Is Treated as NaN.
*/